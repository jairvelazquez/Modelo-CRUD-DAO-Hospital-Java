    @Override
    public void actionPerformed(ActionEvent ae) {
        Object entrada = ae.getSource();
        
        if(entrada.equals(btnAddPacient)){
            String nombre = txtNombre1.getText();
            int id = Integer.parseInt(txtID.getText());
            String ap1 = txtApellido1.getText();
            String ap2 = txtApellido2.getText();
            int antiguedad = Integer.parseInt(txtAntiguedad.getText());
            Urgenciologo urgenciologo = new Urgenciologo(nombre,ap1,ap2,antiguedad);
            urgenciologo.setId(id);
            IUrgenciologoDAO dao= new UrgenciologoDaoImpl();
            dao.actualizar(urgenciologo);
            this.setVisible(false);
        }
        else if(entrada.equals(btnClean)){
            txtNombre1.setText("");
            txtApellido1.setText("");
            txtApellido2.setText("");
            txtAntiguedad.setText("");
        }
    }

